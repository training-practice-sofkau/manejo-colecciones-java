package org.example.list;

public class Tienda {
    int arepas;
    int leche;
    int huevos;
    int cafe;

    public Tienda(int arepas, int leche, int huevos, int cafe) {
        System.out.println("Cantidad Arepas");
        System.out.println(this.arepas = arepas);
        System.out.println("Cantidad leche");
        System.out.println(this.leche = leche);
        System.out.println("Cantidad huevos");
        System.out.println(this.huevos = huevos);
        System.out.println("Cantidad cafe");
        System.out.println(this.cafe = cafe);
    }
}
